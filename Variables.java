public class Variables {
    public static void main(String[] args) {
        // Объявление и инициализация переменных разных типов данных
        int age = 25; // целочисленная переменная
        double height = 1.75; // переменная с плавающей точкой
        char gender = 'M'; // символьная переменная
        boolean isStudent = true; // логическая переменная
        String name = "John"; // строковая переменная

        // Вывод информации, содержащей переменные
        System.out.println("Имя: " + name);
        System.out.println("Возраст: " + age);
        System.out.println("Рост: " + height + " м");
        System.out.println("Пол: " + gender);
        System.out.println("Студент: " + isStudent);
    }
}
